num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

#addition
add = num1 + num2
print("Addition:", add)

#subtraction
sub = num1 - num2
print("Subtraction", sub)

#multiplication
mul = num1 * num2
print("Multiplication", mul)

#division
divi = num1 / num2
print("Division:", round(divi, 2))